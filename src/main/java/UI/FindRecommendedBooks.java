    private void searchBookButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBookButtonActionPerformed
        String bookName = searchBookTitleTextField.getText();
        int index = ba.search(bookName);

        if (index != -1) {
            recommendedBooksTextArea.setText(ba.recBooksByName(index));
        } else {
            JOptionPane.showMessageDialog(null, "The book you searched for does not exist in this library :(", "Book not found", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_searchBookButtonActionPerformed

