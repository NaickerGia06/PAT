    private void searchGenreButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchGenreButtonActionPerformed
        String getGenre = (String) searchGenreComboBox.getSelectedItem();
        String recBooksGenre = ba.recBooksByGenre(getGenre);
        if (recBooksGenre.isBlank()) {
            JOptionPane.showMessageDialog(null, "There are no books in the library with this genre", "Books not found", JOptionPane.INFORMATION_MESSAGE);
        } else {
            recommendedBooksTextArea.setText(recBooksGenre);
        }
    }//GEN-LAST:event_searchGenreButtonActionPerformed

